"""Константы интерфейса стенда заказчика (ПИВ / ICS).

Единый источник правды по ICD: единицы, пределы органов управления, биты маски валидности,
условия включения и перевод шкал. Всё, что описано в `ICSInterface.cs`, живёт здесь, а не
инлайном в `sim_interface.py`, — чтобы правка после ответа разработчика стенда была в одном месте.

**Кодировка транспорта:** чистый JSON поверх UDP (`Struct → Newtonsoft.Json.JsonConvert → string →
Encoding.UTF8.GetBytes()`), адрес берётся из UDP-заголовка входящего пакета. Ни заголовка, ни CRC,
ни серийных номеров — поэтому никакого кадрирования на нашей стороне быть не должно.

**Осторожно с `ICSInterface.cs`:** это неполный срез, а не исчерпывающий контракт. Поле
`AgentIsActive` в нём отсутствует, но стендом обрабатывается. Расхождение с этим файлом само по
себе не является дефектом — сверяться нужно с работающим стендом.
"""

