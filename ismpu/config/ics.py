class ControlValid(IntFlag):
    """Биты `ControlValidMask`: какие каналы несёт команда.

    **ТРЕБУЕТ ПОДТВЕРЖДЕНИЯ.** В `ICSInterface.cs` это голый `int` без констант. Достоверно
    известен только бит 0 — в примере `ics_connector.main()` значение 1 стоит вместе с
    `ElevatorCmd`. Остальная раскладка взята по реализации второго участника НИР (тот же ICD) и
    согласуется с известным битом 0. Отсутствующий бит означает «канал не задан», а не «ноль».
    """
    ELEVATOR = 1 << 0        # подтверждено примером main()
    AILERON = 1 << 1
    RUDDER = 1 << 2
    THROTTLE_RATE = 1 << 3
    THROTTLE_NORM = 1 << 4
    AIRBRAKE = 1 << 5
    NOSE_WHEEL_TILLER = 1 << 6
    RUDDER_PEDAL = 1 << 7
    WHEEL_BRAKES = 1 << 8
    REVERSE = 1 << 9


ROLLOUT_CONTROL_MASK = (ControlValid.RUDDER | ControlValid.NOSE_WHEEL_TILLER
                        | ControlValid.WHEEL_BRAKES | ControlValid.REVERSE
                        | ControlValid.THROTTLE_NORM)
"""Каналы, которыми управляет контур пробега. Продольные органы (руль высоты, элероны,
воздушный тормоз) мы не формируем и заявлять не должны: подтвердить управление каналом, который
не выдаёшь, значит взять на себя ответственность за неуправляемый актуатор."""

