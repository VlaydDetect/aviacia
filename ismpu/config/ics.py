class ControlValid(IntFlag):
    """Биты `ControlValidMask`: какие каналы несёт команда.

    **Один бит на одно командное поле `ICSOutputs`, в порядке их объявления.** В структуре ровно
    14 командных полей (от `ElevatorCmd` до `ReverseRightCmd`, дальше идут флаги режимов), и у
    второго участника НИР ровно столько же битов: `CONTROL_COMMAND_COUNT = 14`,
    `ALL_CONTROL_VALID_MASK = 16383` (`roman_aviacia_ics/tools/ics_protocol.py`).

    Раскладка сходится с обоими независимыми наблюдениями со стенда:

    * бит 0 = `ElevatorCmd` (в примере `ics_connector.main()` маска 1 стоит вместе с рулём высоты,
      и на стенде так же работает `ics_elevator_authority_sweep.py`);
    * маска **31** = первые пять полей, что дословно совпадает с описанием рабочего воздушного
      прогона коллеги: «only elevator, aileron, rudder, and the two throttle rate commands».

    Прежняя раскладка в этом файле объединяла левый и правый каналы в один бит (`WHEEL_BRAKES`,
    `REVERSE`, `THROTTLE_RATE`) и потому расходилась со стендом уже с бита 3: заявленная нами
    маска пробега попадала не в те каналы. Отсутствующий бит означает «канал не задан», а не
    «ноль».
    """
    ELEVATOR = 1 << 0
    AILERON = 1 << 1
    RUDDER = 1 << 2
    THROTTLE_LEFT_RATE = 1 << 3
    THROTTLE_RIGHT_RATE = 1 << 4
    THROTTLE_LEFT = 1 << 5
    THROTTLE_RIGHT = 1 << 6
    NOSE_WHEEL_TILLER = 1 << 7
    RUDDER_PEDAL = 1 << 8
    BRAKE_LEFT = 1 << 9
    BRAKE_RIGHT = 1 << 10
    AIRBRAKE = 1 << 11
    REVERSE_LEFT = 1 << 12
    REVERSE_RIGHT = 1 << 13


CONTROL_COMMAND_COUNT = 14
ALL_CONTROL_MASK = (1 << CONTROL_COMMAND_COUNT) - 1
"""Все командные поля разом (16383). Управлением не пользуемся — только для диагностики."""

ROLLOUT_CONTROL_MASK = (ControlValid.RUDDER | ControlValid.RUDDER_PEDAL
                        | ControlValid.BRAKE_LEFT | ControlValid.BRAKE_RIGHT
                        | ControlValid.THROTTLE_LEFT_RATE | ControlValid.THROTTLE_RIGHT_RATE
                        | ControlValid.REVERSE_LEFT | ControlValid.REVERSE_RIGHT)
"""Каналы пробега: руль направления, **педальный пост**, оба тормоза, скорости обоих РУД (ими
задаётся обратная тяга) и обе створки реверса.

Тиллера здесь нет — по таблице Заказчика он «используется на рулении», а на пробеге путевое
управление идёт педальным постом. Продольные органы (руль высоты, элероны, воздушный тормоз) мы
на пробеге не формируем и заявлять не должны: подтвердить управление каналом, который не выдаёшь,
значит взять на себя ответственность за неуправляемый актуатор."""

TAXI_CONTROL_MASK = (ControlValid.NOSE_WHEEL_TILLER
                     | ControlValid.BRAKE_LEFT | ControlValid.BRAKE_RIGHT
                     | ControlValid.THROTTLE_LEFT_RATE | ControlValid.THROTTLE_RIGHT_RATE)
"""Каналы руления: тиллер вместо педального поста и руля направления (на скоростях руления
аэродинамический руль бесполезен), тормоза и тяга. Реверс на рулении не применяется."""

AIRBORNE_CONTROL_MASK = (ControlValid.ELEVATOR | ControlValid.AILERON | ControlValid.RUDDER
                         | ControlValid.THROTTLE_LEFT_RATE | ControlValid.THROTTLE_RIGHT_RATE)
"""Каналы воздушного участка: руль высоты, элероны, руль направления и **темпы** обоих РУД.

Числовое значение — **31**, и это единственная маска, подтверждённая живым стендом: именно с ней
второй участник НИР провёл заход от 400+ футов до касания (`roman_aviacia_ics/tools/run_ics_pid.py`).

Абсолютное положение РУД (`ThrottleLeft/Right`) здесь намеренно **не** заявлено, хотя и
передаётся: в воздухе управляем темпом, а положение дублируется лишь на случай сборки стенда,
игнорирующей маску, — оба канала при этом ведут к одной уставке и не спорят друг с другом."""

